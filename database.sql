SELECT '--- USERS ---' AS '';
SELECT UserId, Name, Email, Role FROM Users;

SELECT '--- HOTELS ---' AS '';
SELECT * FROM Hotels;

SELECT '--- AMENITIES ---' AS '';
SELECT * FROM Amenities;

SELECT '--- ROOMS ---' AS '';
SELECT r.RoomId, h.Name AS Hotel, r.RoomType, r.PricePerNight, r.Capacity
FROM Rooms r
JOIN Hotels h ON r.HotelId = h.HotelId;

SELECT '--- ROOM AMENITIES ---' AS '';
SELECT r.RoomId, r.RoomType, h.Name AS Hotel, GROUP_CONCAT(a.Name SEPARATOR ', ') AS Amenities
FROM Rooms r
JOIN Hotels h ON r.HotelId = h.HotelId
JOIN RoomAmenities ra ON r.RoomId = ra.RoomId
JOIN Amenities a ON ra.AmenityId = a.AmenityId
GROUP BY r.RoomId, r.RoomType, h.Name;

SELECT '--- COUPONS ---' AS '';
SELECT * FROM Coupons;

SELECT '--- BOOKINGS ---' AS '';
SELECT b.BookingId, u.Name AS User, h.Name AS Hotel, r.RoomType,
       b.CheckInDate, b.CheckOutDate, b.TotalAmount, b.DiscountAmount, b.ReservationNumber
FROM Bookings b
JOIN Users u ON b.UserId = u.UserId
JOIN Rooms r ON b.RoomId = r.RoomId
JOIN Hotels h ON r.HotelId = h.HotelId;


