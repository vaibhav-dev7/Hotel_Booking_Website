DROP DATABASE IF EXISTS HotelBookingDB;
CREATE DATABASE HotelBookingDB;
USE HotelBookingDB;


-- ============================================================
-- STEP 2: CREATE TABLES (in order of dependencies)
-- ============================================================

-- TABLE 1: Users
-- Stores all registered users (customers + admins)
CREATE TABLE Users (
    UserId INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    Email VARCHAR(150) NOT NULL UNIQUE,
    PasswordHash VARCHAR(255) NOT NULL,
    Role VARCHAR(20) NOT NULL DEFAULT 'User',
    CreatedAt DATETIME DEFAULT CURRENT_TIMESTAMP
);

-- TABLE 2: Hotels
-- Stores hotel information that users browse
CREATE TABLE Hotels (
    HotelId INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(150) NOT NULL,
    Location VARCHAR(150) NOT NULL,
    Description TEXT NULL
);

-- TABLE 3: Amenities
-- Reusable amenity names shared across rooms
CREATE TABLE Amenities (
    AmenityId INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL
);

-- TABLE 4: Rooms
-- Each room belongs to a hotel
CREATE TABLE Rooms (
    RoomId INT AUTO_INCREMENT PRIMARY KEY,
    HotelId INT NOT NULL,
    RoomType VARCHAR(100) NOT NULL,
    PricePerNight DECIMAL(10,2) NOT NULL,
    Capacity INT NOT NULL,
    IsActive BOOLEAN DEFAULT TRUE,
    FOREIGN KEY (HotelId) REFERENCES Hotels(HotelId) ON DELETE CASCADE
);

-- TABLE 5: RoomAmenities (Junction / Mapping Table)
-- Many-to-Many: which room has which amenities
CREATE TABLE RoomAmenities (
    RoomId INT NOT NULL,
    AmenityId INT NOT NULL,
    PRIMARY KEY (RoomId, AmenityId),
    FOREIGN KEY (RoomId) REFERENCES Rooms(RoomId) ON DELETE CASCADE,
    FOREIGN KEY (AmenityId) REFERENCES Amenities(AmenityId) ON DELETE CASCADE
);

-- TABLE 6: Coupons
-- Discount codes that users can apply during booking
CREATE TABLE Coupons (
    CouponId INT AUTO_INCREMENT PRIMARY KEY,
    Code VARCHAR(50) NOT NULL UNIQUE,
    DiscountPercentage DECIMAL(5,2) NOT NULL,
    ExpiryDate DATE NOT NULL,
    IsActive BOOLEAN DEFAULT TRUE
);

-- TABLE 7: Bookings (Core Table)
-- Every booking gets a row here
CREATE TABLE Bookings (
    BookingId INT AUTO_INCREMENT PRIMARY KEY,
    UserId INT NOT NULL,
    RoomId INT NOT NULL,
    CheckInDate DATE NOT NULL,
    CheckOutDate DATE NOT NULL,
    TotalAmount DECIMAL(10,2) NOT NULL,
    CouponId INT NULL,
    DiscountAmount DECIMAL(10,2) DEFAULT 0,
    ReservationNumber VARCHAR(100) NOT NULL UNIQUE,
    CreatedAt DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (UserId) REFERENCES Users(UserId),
    FOREIGN KEY (RoomId) REFERENCES Rooms(RoomId),
    FOREIGN KEY (CouponId) REFERENCES Coupons(CouponId)
);


